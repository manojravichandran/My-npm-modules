//Addition
exports.add = function add (num1, num2) {
  
 
   return (num1) + (num2)
 }
 //Subtraction
 exports.sub = function add (num1, num2) {
  
 
   return (num1) - (num2)
 }
 //Multiply
 exports.multiply = function multiply (num1, num2) {
 
   return (num1) * (num2)
 }
 //Divide
 exports.divide = function divide (num1, num2) {

 
   return (num1) / (num2)
 }
 //Modulo
 exports.modulo = function modulo (num1, num2) {

 
   return (num1) % (num2)
 }
 //Increment
 exports.inc = function increment (num) {

 
   return num+1
 }
 //Decrement
 exports.dec = function decrement (num) {

 
   return num-1
 }
 //Exponent
 exports.expo = function exponent (num1, num2) {

 
   return (num1) ** (num2)
 }